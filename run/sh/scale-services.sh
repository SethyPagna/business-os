#!/usr/bin/env sh
set -eu

ROOT="${BUSINESS_OS_REPO_ROOT:-$(CDPATH= cd -- "$(dirname -- "$0")/../.." && pwd)}"
cd "$ROOT"

if [ "${1:-}" = "" ]; then
  echo "Usage: scale-services.sh up|down|status|logs"
  exit 2
fi

node ops/scripts/verify-scale-services.js --require --print-compose-command

export DOCKER_CONFIG="$ROOT/ops/runtime/docker-config"
mkdir -p "$DOCKER_CONFIG" "$ROOT/ops/runtime/secrets"
SCALE_ENV="$ROOT/ops/runtime/docker-scale.env"
if [ -f "$ROOT/minio.license" ]; then
  cp "$ROOT/minio.license" "$ROOT/ops/runtime/secrets/minio.license"
elif [ ! -f "$ROOT/ops/runtime/secrets/minio.license" ]; then
  : > "$ROOT/ops/runtime/secrets/minio.license"
fi
export MINIO_LICENSE_HOST_FILE="$ROOT/ops/runtime/secrets/minio.license"

PROFILE="${BUSINESS_OS_DOCKER_PROFILE:-performance}"
case "$PROFILE" in
  balanced|performance|maximum) ;;
  *) PROFILE="performance" ;;
esac
cat > "$SCALE_ENV" <<EOF
# Generated by Business OS scale-services.sh. Safe to delete; it will be recreated.
# Secrets are intentionally not stored here.
BUSINESS_OS_DOCKER_PROFILE=$PROFILE
REDIS_QUEUE_BIND_HOST=127.0.0.1
REDIS_QUEUE_PORT=6379
REDIS_QUEUE_MAX_MEMORY=${REDIS_QUEUE_MAX_MEMORY:-1536mb}
REDIS_QUEUE_MEMORY_LIMIT=${REDIS_QUEUE_MEMORY_LIMIT:-1536m}
REDIS_QUEUE_CPUS=${REDIS_QUEUE_CPUS:-1.0}
REDIS_CACHE_BIND_HOST=127.0.0.1
REDIS_CACHE_PORT=6380
REDIS_CACHE_MAX_MEMORY=${REDIS_CACHE_MAX_MEMORY:-768mb}
REDIS_CACHE_MEMORY_LIMIT=${REDIS_CACHE_MEMORY_LIMIT:-768m}
REDIS_CACHE_CPUS=${REDIS_CACHE_CPUS:-0.5}
POSTGRES_BIND_HOST=127.0.0.1
POSTGRES_PORT=${POSTGRES_PORT:-55432}
POSTGRES_MEMORY_LIMIT=${POSTGRES_MEMORY_LIMIT:-4608m}
POSTGRES_CPUS=${POSTGRES_CPUS:-4.0}
POSTGRES_SHM_SIZE=${POSTGRES_SHM_SIZE:-1g}
POSTGRES_SHARED_BUFFERS=${POSTGRES_SHARED_BUFFERS:-1GB}
POSTGRES_EFFECTIVE_CACHE_SIZE=${POSTGRES_EFFECTIVE_CACHE_SIZE:-3GB}
POSTGRES_WORK_MEM=${POSTGRES_WORK_MEM:-32MB}
POSTGRES_MAINTENANCE_WORK_MEM=${POSTGRES_MAINTENANCE_WORK_MEM:-512MB}
POSTGRES_MAX_WAL_SIZE=${POSTGRES_MAX_WAL_SIZE:-4GB}
POSTGRES_CHECKPOINT_TIMEOUT=${POSTGRES_CHECKPOINT_TIMEOUT:-20min}
MINIO_BIND_HOST=127.0.0.1
MINIO_MEMORY_LIMIT=${MINIO_MEMORY_LIMIT:-1536m}
MINIO_CPUS=${MINIO_CPUS:-1.5}
APP_BIND_HOST=127.0.0.1
APP_MEMORY_LIMIT=${APP_MEMORY_LIMIT:-1024m}
APP_CPUS=${APP_CPUS:-1.5}
IMPORT_WORKER_MEMORY_LIMIT=${IMPORT_WORKER_MEMORY_LIMIT:-2560m}
IMPORT_WORKER_CPUS=${IMPORT_WORKER_CPUS:-3.0}
MEDIA_WORKER_MEMORY_LIMIT=${MEDIA_WORKER_MEMORY_LIMIT:-3072m}
MEDIA_WORKER_CPUS=${MEDIA_WORKER_CPUS:-3.5}
IMPORT_QUEUE_CONCURRENCY=${IMPORT_QUEUE_CONCURRENCY:-3}
MEDIA_QUEUE_CONCURRENCY=${MEDIA_QUEUE_CONCURRENCY:-4}
IMPORT_ROW_BATCH_SIZE=${IMPORT_ROW_BATCH_SIZE:-400}
IMPORT_BATCH_PAUSE_MS=${IMPORT_BATCH_PAUSE_MS:-20}
IMPORT_IMAGE_CONCURRENCY=${IMPORT_IMAGE_CONCURRENCY:-4}
RUNTIME_CACHE_ENABLED=${RUNTIME_CACHE_ENABLED:-1}
UPLOAD_CHUNK_MB=${UPLOAD_CHUNK_MB:-12}
EOF

DOCKER_EXE="${DOCKER_EXE:-docker}"
COMPOSE="$ROOT/ops/docker/compose.scale.yml"

case "$1" in
  up)
    "$DOCKER_EXE" compose --env-file "$SCALE_ENV" -f "$COMPOSE" up -d --remove-orphans
    ;;
  down)
    "$DOCKER_EXE" compose --env-file "$SCALE_ENV" -f "$COMPOSE" down
    ;;
  status)
    "$DOCKER_EXE" compose --env-file "$SCALE_ENV" -f "$COMPOSE" ps
    ;;
  logs)
    "$DOCKER_EXE" compose --env-file "$SCALE_ENV" -f "$COMPOSE" logs --tail=200
    ;;
  *)
    echo "Unknown command: $1"
    exit 2
    ;;
esac
